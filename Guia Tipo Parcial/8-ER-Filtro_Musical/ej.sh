#!/bin/bash

r1="\(00:30:[0-5][0-9]\)"; #00:30:00 - 00:30:59
r2="\(00:3[1-9]:[0-5][0-9]\)"; #00:31:00 - 00:39:59
r3="\(00:[4-5][0-9]:[0-5][0-9]\)"; #00:40:00 - 00:59:59
r4="\(01:0[0-9]:[0-5][0-9]\)"; #01:00:00 - 01:09:59
r5="\(01:1[0-9]:[0-5][0-9]\)"; #01:10:00 - 01:19:59
r6="\(01:20:00\)";

lineas=$(grep "^[^;]*;[^;]*;[^;]*;$r1\|$r2\|$r3\|$r4\|$r5\|$r6.*" "Lista.Musical" | sed "s/^[^;]*;[^;]*;\([^;]*\);[^;]*;\([^;]*\);.*/\2-\1/");

echo "$lineas";